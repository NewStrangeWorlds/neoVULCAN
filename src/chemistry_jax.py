# Vectorise over atmospheric layers.
# Calling convention:  y(nz,ni), M(nz,), k_array(nr+1,nz)  ->  dydt(nz,ni)
_chemdf_vmap = jax.vmap(_chemdf_single, in_axes=(0, 0, 1))
chemdf_jax   = jax.jit(_chemdf_vmap)

# Jacobian d(dydt_i)/d(y_j) for a single layer  ->  (ni, ni)
# jacfwd uses forward-mode AD: ni pushforwards for an (ni,)->(ni,) function.
_jac_single = jax.jacfwd(_chemdf_single, argnums=0)
_jac_vmap   = jax.vmap(_jac_single, in_axes=(0, 0, 1))
_jac_jit    = jax.jit(_jac_vmap)


