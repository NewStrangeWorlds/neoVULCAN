def chemdf(y, M, k_dict):
    """Drop-in for chem_funs.chemdf(y, M, k).

    y      : (nz, ni) numpy array
    M      : (nz,)    numpy array
    k_dict : dict {{reaction_index: array(nz)}}

    Returns (nz, ni) numpy array of dn/dt from chemistry.
    """
    k_arr = k_dict_to_array(k_dict)
    return np.asarray(chemdf_jax(jnp.asarray(y),
                                 jnp.asarray(M),
                                 jnp.asarray(k_arr)))


def neg_achemjac(y, M, k_dict):
    """Drop-in for chem_funs.neg_symjac(y, M, k).

    Returns the *negative* chemistry Jacobian as a dense (ni*nz, ni*nz)
    NumPy array (block-diagonal; diffusion coupling added by the caller).
    """
    nz, ni = y.shape
    k_arr  = k_dict_to_array(k_dict)

    jac_blocks = _jac_jit(jnp.asarray(y),
                          jnp.asarray(M),
                          jnp.asarray(k_arr))         # (nz, ni, ni)
    jac_np = np.asarray(jac_blocks)                   # back to CPU NumPy

    # Build block-diagonal (ni*nz, ni*nz) matrix from (nz, ni, ni) blocks.
    dfdy = _scipy_block_diag(*jac_np)
    return -dfdy
