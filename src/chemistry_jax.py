def k_dict_to_array(k_dict):
    """Convert k dict {{1..nr: array(nz)}} to a (nr+1, nz) numpy array."""
    nr  = max(k_dict.keys())
    nz  = len(next(iter(k_dict.values())))
    arr = np.zeros((nr + 1, nz), dtype=np.float64)
    for i, v in k_dict.items():
        arr[i] = v
    return arr


